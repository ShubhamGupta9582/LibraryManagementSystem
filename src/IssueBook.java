    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(jTextField15.getText().isEmpty() || jTextField15.getText().equals(null) || jTextField15.getText().equals("null")){
        
            JOptionPane.showMessageDialog(null, "Empty Issue Date or Invalid Values");
        }
        else{
        
            try{
                
                String str = "select * from IssueBook where book_id = ?";
                ps = conn.prepareStatement(str);
                ps.setString(1, jTextField1.getText());
                rs = ps.executeQuery();
                if(rs.next()){
                
                    JOptionPane.showMessageDialog(null, "Book is already issued");
                }
                else{
        
            String sql = "insert into IssueBook values(?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)";
            ps = conn.prepareStatement(sql);
            ps.setString(1, jTextField1.getText());
            ps.setString(2, jTextField2.getText());
            ps.setString(3, jTextField3.getText());
            ps.setString(4, jTextField4.getText());
            ps.setString(5, jTextField5.getText());
            ps.setString(6, jTextField6.getText());
            ps.setString(7, jTextField7.getText());
            ps.setString(8, jTextField8.getText());
            ps.setString(9, jTextField9.getText());
            ps.setString(10, jTextField10.getText());
            ps.setString(11, jTextField11.getText());
            ps.setString(12, jTextField12.getText());
            ps.setString(13, jTextField13.getText());
            ps.setString(14, jTextField14.getText());
            ps.setString(15, jTextField15.getText());
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Book Issued");
            conn.close();
                }
            }
            catch(Exception ex){
        
            JOptionPane.showMessageDialog(null, ex);
            }
            
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    
