    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        if((jTextField2.getText().isEmpty() || jTextField2.getText().equals(null) || jTextField2.getText().equals("null")) ||
           (jComboBox1.getSelectedItem().equals("Select") || jComboBox1.getSelectedItem().equals(null) || jComboBox1.getSelectedItem().equals("null")) ||               
           (jTextField4.getText().isEmpty() || jTextField4.getText().equals(null) || jTextField4.getText().equals("null")) ||
           (jTextField5.getText().isEmpty() || jTextField5.getText().equals(null) || jTextField5.getText().equals("null")) ||
           (jTextField6.getText().isEmpty() || jTextField6.getText().equals(null) || jTextField6.getText().equals("null")) ||
           (jTextField7.getText().isEmpty() || jTextField7.getText().equals(null) || jTextField7.getText().equals("null"))){
            
                JOptionPane.showMessageDialog(null, "Invalid Values");
        }
        else{
        
            try{
            
                String sql = "insert into Book values(?,?,?,?,?,?,?)";
                ps = conn.prepareStatement(sql);
                ps.setString(1, jTextField1.getText());
                ps.setString(2, jTextField2.getText());
                ps.setString(3, (String)jComboBox1.getSelectedItem());
                ps.setString(4, jTextField4.getText());
                ps.setString(5, jTextField5.getText());
                ps.setString(6, jTextField6.getText());
                ps.setString(7, jTextField7.getText());
                ps.executeUpdate();
                conn.close();
                JOptionPane.showMessageDialog(null, "New Book Added");
            }
            catch(Exception ex){
            
                JOptionPane.showMessageDialog(null, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    
